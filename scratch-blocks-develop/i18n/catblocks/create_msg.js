#!/usr/bin/env node

/**
 * @author Andreas Karner <andreas.karner@student.tugraz.at>
 * @description generate crowdin message file based on msg/*.json files, please run first create_msg_json.js if needed
 *  
 * @changelog 2019-08-14: initial version
 */

const fs = require('fs');
const path = require('path');

// please define config here
const MSG_DIR = path.join('msg');
const JSON_DIR = path.join(MSG_DIR, 'json');
const JS_DIR = path.join(MSG_DIR, 'js');
const MESSAGE_FILE = path.join(MSG_DIR, 'catblocks_msgs.js');
const JS_HEADER = `// This file was automatically generated by ${path.basename(__filename)}
// Please do not modify.

'use strict';

`;
const MESSAGE_HEADER = `// This file was automatically generated by ${path.basename(__filename)}
// Please do not modify.

'use strict';

goog.provide('Blockly.ScratchMsgs.allLocales');
goog.require('Blockly.ScratchMsgs');

`;


// no more config down here

// generate main catblocks_msgs.js file
const message_js_fd = fs.openSync(MESSAGE_FILE, 'w');
fs.writeSync(message_js_fd, MESSAGE_HEADER);

// generate for each msg/*.json file a msg/*.js file
const LANG_FILES = fs.readdirSync(JSON_DIR, { encoding: 'utf-8' });
LANG_FILES.forEach(langfile => {
  if (langfile.match(/.+\.json$/)) {
    const lang_name = langfile.substr(0, langfile.indexOf('.'))
    const lang_json_path = path.join(JSON_DIR, langfile);
    const lang_json_object = JSON.parse(fs.readFileSync(lang_json_path, { encoding: 'utf-8' }));

    const lang_js_path = path.join(JS_DIR, `${lang_name}.js`);
    const lang_js_fd = fs.openSync(lang_js_path, 'w');
    fs.writeSync(lang_js_fd, JS_HEADER);
    fs.writeSync(lang_js_fd, `export default {\n`);

    Object.keys(lang_json_object).forEach(key => {
      fs.writeSync(lang_js_fd, `  "${key}": "${lang_json_object[key]}",\n`);
    });
    fs.writeSync(lang_js_fd, `};`);
    fs.closeSync(lang_js_fd);

    // link it to the global catblocks message file
    fs.writeSync(message_js_fd, `import ${lang_name} from './js/${lang_name}.js'\n`);
    fs.writeSync(message_js_fd, `Blockly.ScratchMsgs.locales["${lang_name}"] = ${lang_name};\n`);
  }
});

fs.closeSync(message_js_fd);